// Weather phrase categories based on weather code and temperature
export type WeatherCategory = 
  | 'sunny_hot' 
  | 'sunny_mild' 
  | 'cloudy' 
  | 'rain' 
  | 'storm' 
  | 'snow' 
  | 'fog' 
  | 'cold' 
  | 'windy';

export type SupportedLanguage = 'pt-BR' | 'en-US' | 'es-ES' | 'fr-FR' | 'de-DE' | 'it-IT' | 'ja-JP' | 'ko-KR' | 'zh-CN';

export interface WeatherPhrase {
  text: string;
  emoji: string;
}

// Map weather code to category
export function getWeatherCategory(weatherCode: number, temperature: number): WeatherCategory {
  // Thunderstorm
  if (weatherCode >= 95) return 'storm';
  
  // Snow
  if (weatherCode >= 71 && weatherCode <= 77) return 'snow';
  
  // Rain/Drizzle
  if ((weatherCode >= 51 && weatherCode <= 67) || (weatherCode >= 80 && weatherCode <= 82)) return 'rain';
  
  // Fog
  if (weatherCode >= 45 && weatherCode <= 48) return 'fog';
  
  // Cloudy
  if (weatherCode >= 1 && weatherCode <= 3) return 'cloudy';
  
  // Clear sky - check temperature
  if (weatherCode === 0) {
    if (temperature >= 30) return 'sunny_hot';
    if (temperature <= 10) return 'cold';
    return 'sunny_mild';
  }
  
  // Default based on temperature
  if (temperature >= 30) return 'sunny_hot';
  if (temperature <= 10) return 'cold';
  
  return 'cloudy';
}

