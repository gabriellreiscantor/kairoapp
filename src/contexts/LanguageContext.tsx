/**
 * Gets the current system/browser language without checking localStorage.
 * Used for detecting system language changes.
 */
export const getSystemLanguage = (): LanguageCode => {
  const browserLang = navigator.language || (navigator as any).userLanguage;
  
  // Try exact match first
  if (translations[browserLang as LanguageCode]) {
    return browserLang as LanguageCode;
  }
  
  // Try matching just the language code (e.g., 'en' from 'en-GB')
  const langCode = browserLang.split('-')[0];
  const matchingLang = Object.keys(translations).find(key => 
    key.startsWith(langCode)
  ) as LanguageCode;
  
  if (matchingLang) {
    return matchingLang;
  }
  
  return 'en-US';
};

/**
 * Detects the device language and returns a supported language code.
 * Priority: 1. LocalStorage (user preference), 2. System language, 3. English
 */
export const detectDeviceLanguage = (): LanguageCode => {
  const savedLanguage = localStorage.getItem('kairo-language') as LanguageCode;
  if (savedLanguage && translations[savedLanguage]) {
    return savedLanguage;
  }
  
  return getSystemLanguage();
};

interface LanguageProviderProps {
  children: ReactNode;
}

export const LanguageProvider = ({ children }: LanguageProviderProps) => {
  const [language, setLanguageState] = useState<LanguageCode>(() => detectDeviceLanguage());

  useEffect(() => {
    // Update HTML lang attribute
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = async (lang: LanguageCode) => {
    setLanguageState(lang);
    localStorage.setItem('kairo-language', lang);
    document.documentElement.lang = lang;
    
    // ‚úÖ Emit event for other components (like CallKit) to react to language change
    console.log('[Language] Emitting horah:language-changed event:', lang);
    window.dispatchEvent(new CustomEvent('horah:language-changed', { detail: { language: lang } }));
    
    // ‚úÖ Sync language to database for MeLig/TTS to use
    try {
      const { data: { user } } = await supabase.auth.getUser();
      if (user) {
        await supabase
          .from('profiles')
          .update({ language: lang })
          .eq('id', user.id);
        console.log('[Language] Synced to database:', lang);
      }
    } catch (e) {
      console.error('[Language] Failed to sync to database:', e);
    }
  };

  const t = (key: string): string => {
    const langTranslations = translations[language];
    if (langTranslations && langTranslations[key]) {
      return langTranslations[key];
    }
    
    // Fallback to English
    const fallback = translations['en-US'][key];
    if (fallback) {
      return fallback;
    }
    
    // Return key if no translation found
    return key;
  };

  const getDateLocale = (): Locale => {
    return DATE_LOCALES[language] || ptBR;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, getDateLocale }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

